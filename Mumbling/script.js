function accum(s) {
   let chars = s.split('');
   let result = '';
   let arr = [];
   for (const [i, char] of chars.entries()) {
      let part = '';
      for (let j = 0; j <= i; j++) {
         if (j == 0) {
            part += char.toUpperCase();
         } else {
            part += char.toLowerCase();
         }
      }
      arr.push(part);
   }
   result = arr.join('-');
   return result;
}



